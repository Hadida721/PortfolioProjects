-- Removed Duplicates
WITH RowNumCTE AS (
SELECT *,
   ROW_NUMBER() OVER (
   PARTITION BY ParcelID,
                PropertyAddress,
				SalePrice,
				LegalReference
				ORDER by 
				   UniqueID
				   ) row_num


From PortfolioProject.dbo.NashvilleHousing
--order by ParcelID
)
-- Used Delete Statement instead of SELECT to remove duplicates then checked if data was removed

SELECT *
From RowNumCTE
Where Row_num > 1
Order By PropertyAddress

